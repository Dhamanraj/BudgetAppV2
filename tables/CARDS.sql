CREATE TABLE `CARDS` (
  `CARD_ID` int NOT NULL AUTO_INCREMENT,
  `CARD_KEY` int NOT NULL,
  `CARD_TYPE` varchar(20) NOT NULL,
  `CARD_NUMBER` varbinary(255) DEFAULT NULL,
  `LAST_4` char(4) DEFAULT NULL,
  `EXP_DATE` varbinary(255) DEFAULT NULL,
  `CVV` varbinary(255) DEFAULT NULL,
  `BANK_ID` int NOT NULL,
  `MEMBER_ID` int NOT NULL,
  `EFF_START_DATE` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `EFF_END_DATE` datetime NOT NULL DEFAULT '9999-12-31 23:59:59',
  `IS_CURRENT` tinyint(1) NOT NULL DEFAULT '1',
  `ADDED_USER` varchar(20) NOT NULL,
  PRIMARY KEY (`CARD_ID`),
  KEY `cards_fk_bank` (`BANK_ID`),
  KEY `cards_fk_member` (`MEMBER_ID`),
  CONSTRAINT `cards_fk_bank` FOREIGN KEY (`BANK_ID`) REFERENCES `BANKS` (`BANK_ID`),
  CONSTRAINT `cards_fk_member` FOREIGN KEY (`MEMBER_ID`) REFERENCES `MEMBERS` (`MEMBER_ID`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;
