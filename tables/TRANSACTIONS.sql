CREATE TABLE `TRANSACTIONS` (
  `TRANSACTION_ID` bigint NOT NULL,
  `TRANSACTION_DATE` datetime NOT NULL,
  `POSTED_DATE` datetime DEFAULT NULL,
  `TRANSACTION_TYPE` varchar(10) NOT NULL,
  `TRANSACTION_AMOUNT` decimal(19,4) NOT NULL,
  `DESCRIPTION` varchar(100) NOT NULL,
  `EXT_DESCRIPTION` varchar(500) DEFAULT NULL,
  `ADDRESS_1` varchar(150) DEFAULT NULL,
  `ADDRESS_2` varchar(50) DEFAULT NULL,
  `CITY` varchar(50) DEFAULT NULL,
  `STATE` varchar(50) DEFAULT NULL,
  `COUNTRY` varchar(50) DEFAULT NULL,
  `ZIPCODE` varchar(20) DEFAULT NULL,
  `CATG_ID` int NOT NULL,
  `SUB_CATG_ID` int NOT NULL,
  `BANK_ID` int NOT NULL,
  `CARD_ID` int NOT NULL,
  `MEMBER_ID` int NOT NULL,
  `ADDED_USER` varchar(20) NOT NULL,
  `ADDED_DATETIME` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`TRANSACTION_ID`),
  KEY `fk_transactions_bank` (`BANK_ID`),
  KEY `fk_transactions_card` (`CARD_ID`),
  KEY `fk_transactions_catg` (`CATG_ID`),
  KEY `fk_transactions_member` (`MEMBER_ID`),
  KEY `fk_transactions_subcatg` (`SUB_CATG_ID`),
  CONSTRAINT `fk_transactions_bank` FOREIGN KEY (`BANK_ID`) REFERENCES `BANKS` (`BANK_ID`),
  CONSTRAINT `fk_transactions_card` FOREIGN KEY (`CARD_ID`) REFERENCES `CARDS` (`CARD_ID`),
  CONSTRAINT `fk_transactions_catg` FOREIGN KEY (`CATG_ID`) REFERENCES `MCC_CATEGORY` (`CATG_ID`),
  CONSTRAINT `fk_transactions_member` FOREIGN KEY (`MEMBER_ID`) REFERENCES `MEMBERS` (`MEMBER_ID`),
  CONSTRAINT `fk_transactions_subcatg` FOREIGN KEY (`SUB_CATG_ID`) REFERENCES `MCC_SUB_CATEGORY` (`SUB_CATG_ID`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;
